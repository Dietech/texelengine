package ch.texelengine.engine.api.textures;

/**
 * Enumerate the different loading options of an image into a texture in a
 * totally generic way
 *
 * @author Dorian Ros
 */
public enum TextureLoadOptions {

    /**
     * No loading option
     */
    NONE,

    /**
     * Flip the loaded image on the y axis
     */
    FLIP_Y
}
