package ch.texelengine.engine.api.buffers;

/**
 * Enumerate the data type that are available in the engine
 *
 * <p>
 * This denomination is completely graphics API independent
 * </p>
 *
 * @author Dorian Ros
 */
public enum DataTypes {

    /**
     * Float type
     */
    FLOAT,

    /**
     * Unsigned integer type
     */
    UINT,

    /**
     * Unsigned byte type
     */
    UBYTE
}
